<script>
import { regPassword } from '@/utils/regrule'
// import { getCaptch } from '@/api/login'

export default {
  name: 'Register',
  data() {
    return {
      codeUrl: 'http://127.0.0.1:7300/api/v1/captcha?type=2&t=',
      registerForm: {
        username: '',
        email: '',
        password: '',
        code: ''
      },
      registerRules: {
        username: [{ required: true, trigger: 'blur', message: '请输入用户名' }],
        email: [
          { required: true, message: '请输入邮箱地址', trigger: 'blur' },
          { type: 'email', message: '请输入正确的邮箱地址', trigger: ['blur', 'change'] }
        ],
        password: [
          { required: true, message: '请输入您的密码', trigger: 'blur' },
          { pattern: regPassword, message: '密码必须是数字和英文字母组合,必须有8位且有一个大写字母' }
        ],
        code: [{ required: true, trigger: 'blur', message: '请输入验证码' }]
      },
      passwordType: 'password',
      loading: false,
      showDialog: false,
      redirect: undefined
    }
  },
  watch: {
    $route: {
      handler: function(route) {
        this.redirect = route.query && route.query.redirect
      },
      immediate: true
    }
  },
  created() {
    // window.addEventListener('hashchange', this.afterQRScan)
  },
  destroyed() {
    // window.removeEventListener('hashchange', this.afterQRScan)
  },
  methods: {
    showPwd() {
      if (this.passwordType === 'password') {
        this.passwordType = ''
      } else {
        this.passwordType = 'password'
      }
    },
    showCode() {
      this.codeUrl = this.codeUrl + new Date().getTime()
    },
    handleLogin() {
      this.$refs.registerForm.validate(valid => {
        if (valid) {
          this.loading = true
          this.$store.dispatch('UserRegister', this.registerForm).then((res) => {
            this.loading = false
            this.$router.push('/login')
          }).catch(() => {
            this.showCode()
            this.loading = false
          })
        } else {
          console.log('error submit!!')
          return false
        }
      })
    },
    afterQRScan() {
      // const hash = window.location.hash.slice(1)
